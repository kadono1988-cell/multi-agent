def main() -> None:
    env = load_env(ENV_FILE)
    pat = env["SUPABASE_ACCESS_TOKEN"]
    ref = env["SUPABASE_PROJECT_REF"]
    url = f"https://api.supabase.com/v1/projects/{ref}/database/query"
    headers = {"Authorization": f"Bearer {pat}", "Content-Type": "application/json",
               "User-Agent": "multi-agent-seed/1.0"}

    def run(sql: str) -> str:
        req = urllib.request.Request(url, data=json.dumps({"query": sql}).encode("utf-8"),
                                     headers=headers, method="POST")
        with urllib.request.urlopen(req, timeout=60) as r:
            return r.read().decode("utf-8")

    print(f"Seeding {len(CASES)} cases ...")
    run("DELETE FROM multi_agent.reference_cases;")

    def esc(s: str) -> str:
        return s.replace("'", "''")

    values = ",\n".join(
        f"('{esc(c['title'])}', '{esc(c['project_type'])}', '{esc(c['summary'])}', '{esc(c['outcome'])}')"
        for c in CASES
    )
    sql = ("INSERT INTO multi_agent.reference_cases (title, project_type, summary, outcome) VALUES\n"
           + values + ";")
    run(sql)

    count = run("SELECT count(*) AS n FROM multi_agent.reference_cases;")
    types = run("SELECT project_type, count(*) FROM multi_agent.reference_cases GROUP BY project_type ORDER BY project_type;")
    print(f"Total: {count}")
    print(f"By type: {types}")


if __name__ == "__main__":
    main()
